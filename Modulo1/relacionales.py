#Operadores Relacionales
"""
Permiten relacinar tipos de datos
numericos.
Se obtiene como resultado un valor
booleano.
	>
	<
	>=
	<=
	==
	!=

"""
numero_uno = 10
numero_dos = 20

resultado = numero_uno > numero_dos
print(resultado)

resultado = numero_uno < numero_dos
print(resultado)

resultado = numero_uno <= numero_dos
print(resultado)

resultado = numero_uno >= numero_dos
print(resultado)

resultado = numero_uno == numero_dos
print(resultado)

resultado = numero_uno != numero_dos
print(resultado)
