"""Tipos de datos
Los tipos de datos en Python Son:
String
Int
Float
Bool"""

#String
titulo_curso = 'Titulo del curso'
print(titulo_curso)

nombre_completo="Jose Perez"
print(nombre_completo)

mensaje='"Codigo Facilito"'
print(mensaje)

mensaje="'Codigo Facilito'"
print(mensaje)

mensaje_amplio="""
			Mensaje amplio con salto de linea 1
			Mensaje amplio con salto de linea 2
			Mensaje amplio con salto de linea 3
			Mensaje amplio con salto de linea 4
"""
print(mensaje_amplio)

#Int
numero_uno=10
print(numero_uno)

numero_uno_menos=-10
print(numero_uno_menos)

#Float
numero_dos = 3.14
print(numero_dos)

numero_dos = -3.14
print(numero_dos)

numero_suma = 10 + 20
print(numero_suma)

numero_resta = 10 - 20
print(numero_resta)

numero_division = 10 / 20
print(numero_division)

numero_division = 10 // 20 #Tipo de dato entero
print(numero_division)

numero_multiplicacion = 10 * 20
print(numero_multiplicacion)

#Bool
valor = True
print(valor)

valor = False
print(valor)