#Operadores logicos
"""
and, or y not

"""

#AND

resultado_final = True and True
print(resultado_final)


resultado_final = True and False and True and (10 > 20)
print(resultado_final)

resultado_final = True and False and True
print(resultado_final)

#OR

resultado_final = True or True
print(resultado_final)

resultado_final = True or False or True
print(resultado_final)

#NOT
resultado_final = not True
print(resultado_final)

resultado_final = not False
print(resultado_final)
